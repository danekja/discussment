package org.danekja.discussment.core.accesscontrol.domain;

import javax.persistence.Column;
import javax.persistence.Embeddable;
import javax.persistence.EnumType;
import javax.persistence.Enumerated;
import java.io.Serializable;

/**
 * Composite primary key for @{@link AbstractPermission} derivates.
 *
 * @author Jakub Danek
 */
@Embeddable
public class PermissionId implements Serializable {

    private String userId;
    private PermissionLevel level;
    private long itemId;
    private PermissionType permissionType;

    PermissionId(String userId, PermissionType permissionType) {
        this.userId = userId;
        this.level = PermissionLevel.GLOBAL;
        this.itemId = 0;
        this.permissionType = permissionType;
    }

    PermissionId(String userId, PermissionLevel level, long itemId, PermissionType permissionType) {
        this.userId = userId;
        this.level = level;
        this.itemId = itemId;
        this.permissionType = permissionType;
    }

    protected PermissionId() {
    }

    @Enumerated(value = EnumType.STRING)
    @Column(name = "permission_type", insertable = false, nullable = false, updatable = false)
    public PermissionType getPermissionType() {
        return permissionType;
    }

    public void setPermissionType(PermissionType permissionType) {
        this.permissionType = permissionType;
    }

    /**
     * Item to which this permission applies. Actual entity depends on the value of
     * {#link permissionType}.
     * @return id of the item (topic, category, post)
     */
    @Column(name = "item_id", nullable = true, updatable = false)
    public Long getItemId() {
        return itemId;
    }

    public void setItemId(Long itemId) {
        this.itemId = itemId;
    }

    /**
     * @return PK of the user which has this permission.
     */
    @Column(name = "user_id", nullable = false, updatable = false)
    public String getUserId() {
        return userId;
    }

    protected void setUserId(String userId) {
        this.userId = userId;
    }

    /**
     * @return Level for which the permission applies (single discussion, topic, category or global)
     */
    @Enumerated(EnumType.STRING)
    @Column(name = "type", nullable = false, updatable = false)
    public PermissionLevel getLevel() {
        return level;
    }

    protected void setLevel(PermissionLevel level) {
        this.level = level;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PermissionId that = (PermissionId) o;

        if (itemId != that.itemId) return false;
        if (!userId.equals(that.userId)) return false;
        return level == that.level;
    }

    @Override
    public int hashCode() {
        int result = userId.hashCode();
        result = 31 * result + level.hashCode();
        result = 31 * result + (int) (itemId ^ (itemId >>> 32));
        return result;
    }

    @Override
    public String toString() {
        return "PermissionId{" +
                "userId='" + userId + '\'' +
                ", level=" + level +
                ", itemId=" + itemId +
                ", permissionType=" + permissionType +
                '}';
    }
}
